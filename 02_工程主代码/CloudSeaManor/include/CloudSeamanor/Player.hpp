#pragma once

// ============================================================================
// 【Player】玩家角色运行时领域对象
// ============================================================================
// 管理玩家的移动、碰撞、朝向与基础视觉状态。
//
// 主要职责：
// - 处理玩家输入并执行移动
// - 碰撞检测（世界边界和障碍物）
// - 管理玩家朝向状态
// - 提供渲染用的图形对象
//
// 与其他系统的关系：
// - 依赖：SFML（Graphics）
// - 被依赖：GameApp（主循环输入处理）、GameAppScene（碰撞数据）
// - 被依赖：GameAppHud（显示玩家状态）、GameAppSave（存档/读档）
//
// 设计说明：
// - 直接暴露SFML图形对象，避免过度封装导致原型期样板代码过多
// - 朝向使用8方向枚举，支持斜向移动
// - 碰撞采用"AABB矩形交集检测"，简单高效
// ============================================================================

#include "CloudSeamanor/MathTypes.hpp"

#include <string>
#include <vector>

namespace CloudSeamanor::domain {

// ============================================================================
// 【FacingDirection】玩家朝向枚举
// ============================================================================
// 支持8个方向（4方向 + 4斜向），用于动画、攻击判定和UI显示。
//
// 使用方式：
// - UI根据朝向显示不同对话气泡方向
// - 攻击/交互系统根据朝向确定影响范围
// - 调试系统将朝向转换为可读文本
enum class FacingDirection {
    Down,       // 向下（默认朝向）
    DownLeft,   // 左下
    Left,       // 向左
    UpLeft,     // 左上
    Up,         // 向上
    UpRight,    // 右上
    Right,      // 向右
    DownRight,  // 右下
};

// ============================================================================
// 【Player】玩家角色领域对象
// ============================================================================
// 管理玩家实体的位置、朝向、移动状态和碰撞。
//
// 设计决策：
// - 使用AABB矩形碰撞检测，简单高效，适合原型期
// - 分轴移动（先X再Y）简化碰撞回退逻辑
// - 直接暴露SFML图形对象，减少封装开销
//
// 使用示例：
// @code
// Player player;
// player.SetMovementState({1, 0}, true);  // 向右移动
// player.Move(delta, world_bounds, obstacles);
// @endcode
class Player {
public:
    // ========================================================================
    // 【Player】构造函数
    // ========================================================================
    // 构造时创建默认占位图形，确保玩家对象可见可测试。
    Player();

    // ========================================================================
    // 【Move】移动玩家（带碰撞检测）
    // ========================================================================
    // @param delta 期望位移向量
    // @param bounds 世界边界（玩家不能超出此范围）
    // @param obstacles 障碍物列表（玩家不能进入）
    // @note 会将位移分解为X轴和Y轴分别处理
    // @note 碰撞时自动回退到碰撞前的位置
    void Move(
        const Vec2f& delta,
        const RectF& bounds,
        const std::vector<RectF>& obstacles
    );

    // ========================================================================
    // 【SetMovementState】设置移动状态和朝向
    // ========================================================================
    // @param direction 移动方向向量（用于计算朝向）
    // @param is_moving 是否正在移动
    // @note 停止移动时保持最后朝向
    // @post 视觉状态（颜色）会根据朝向和移动状态更新
    void SetMovementState(const Vec2f& direction, bool is_moving);

    // ========================================================================
    // 【SetPosition】设置玩家位置
    // ========================================================================
    // @param position 新的位置坐标
    // @note 用于读档、传送、初始化落点等场景
    void SetPosition(const Vec2f& position);

    // ========================================================================
    // 【GetPosition】获取玩家位置
    // ========================================================================
    [[nodiscard]] Vec2f GetPosition() const noexcept { return position_; }

    [[nodiscard]] Vec2f GetSize() const noexcept { return size_; }

    // ========================================================================
    // 【Bounds】获取碰撞包围盒
    // ========================================================================
    // @return 玩家在世界坐标下的AABB包围盒
    // @note 用于交互检测和障碍碰撞
    [[nodiscard]] RectF Bounds() const noexcept;

    // ========================================================================
    // 【Facing】获取当前朝向
    // ========================================================================
    [[nodiscard]] FacingDirection Facing() const noexcept { return facing_; }

    // ========================================================================
    // 【IsMoving】是否正在移动
    // ========================================================================
    [[nodiscard]] bool IsMoving() const noexcept { return is_moving_; }

    // ========================================================================
    // 【FacingText】朝向转可读文本
    // ========================================================================
    // @return 朝向的中文描述（如"上"、"左下"）
    // @note 用于HUD显示和调试日志
    [[nodiscard]] std::string FacingText() const;

    // ========================================================================
    // 【SavePosition】保存位置用于存档
    // ========================================================================
    // @return x,y坐标字符串
    [[nodiscard]] std::string SavePosition() const;

    // ========================================================================
    // 【LoadPosition】从存档恢复位置
    // ========================================================================
    // @param x x坐标
    // @param y y坐标
    void LoadPosition(float x, float y);

private:
    // ========================================================================
    // 【MoveAxis】单轴移动（内部）
    // ========================================================================
    // @param dx X轴位移
    // @param dy Y轴位移
    // @note 先尝试移动，碰撞则回退
    void MoveAxis(float dx, float dy, const std::vector<RectF>& obstacles);

    // ========================================================================
    // 【CollidesWithAny】检测是否与任意障碍物碰撞（内部）
    // ========================================================================
    [[nodiscard]] bool CollidesWithAny(const std::vector<RectF>& obstacles) const noexcept;

    // ========================================================================
    // 成员变量
    // ========================================================================
    Vec2f position_{620.0f, 340.0f};
    Vec2f size_{36.0f, 36.0f};
    FacingDirection facing_ = FacingDirection::Down;  // 当前朝向
    bool is_moving_ = false;                   // 是否正在移动
};

// ============================================================================
// 【FacingFromVector】方向向量转朝向枚举
// ============================================================================
// @param direction 方向向量
// @return 对应的朝向枚举
// @note 优先检测斜向，再检测纯方向
FacingDirection FacingFromVector(const Vec2f& direction);

}  // namespace CloudSeamanor::domain
