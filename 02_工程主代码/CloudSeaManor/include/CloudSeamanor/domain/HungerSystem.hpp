#pragma once

// ============================================================================
// 【HungerSystem】饱食度管理系统
// ============================================================================
// 负责玩家饱食度（Hunger）的值管理、消耗与恢复。
//
// 主要职责：
// - 管理饱食度当前值和上限（100）
// - 处理饱食度消耗（每小时游戏时间、重体力活动）
// - 处理饱食度恢复（食用食材、早餐）
// - 根据饱食度阈值提供效率修正（影响体力恢复/消耗倍率）
//
// 与其他系统的关系：
// - 依赖：GameClock（判断白天/夜晚）
// - 被依赖：StaminaSystem（饱食度影响体力恢复速度）
// - 被依赖：PixelGameHud（饱食度条显示）
// - 被依赖：GameAppSave（存档/读档）
//
// 设计原则：
// - 饱食度是"生存保障"，不是惩罚机制
// - 饥饿只降低效率，不强制结束游戏
// - 每天起床后重置饱食度
// ============================================================================

#include <string>

namespace CloudSeamanor::domain {

// ============================================================================
// 【HungerState】饱食度状态枚举
// ============================================================================
enum class HungerState {
    Satiated,   // 饱足（80~100）：体力恢复+20%
    Normal,     // 正常（40~79）：无加成无惩罚
    Hungry,     // 饥饿（20~39）：恢复-30%，消耗+15%
    Starving,   // 空腹（0~19）：恢复停止，消耗+30%
};

// ============================================================================
// 【HungerSystem】饱食度管理类
// ============================================================================
class HungerSystem {
public:
    // ========================================================================
    // 【Initialize】初始化
    // ========================================================================
    // @param current 初始饱食度值
    // @param max_value 饱食度上限
    void Initialize(int current = 80, int max_value = 100);

    // ========================================================================
    // 【SetGameClock】设置游戏时钟引用（用于白天判断）
    // ========================================================================
    // @note 必须在 Initialize 之后调用
    void SetGameClock(const class GameClock* clock);

    // ========================================================================
    // 【Tick】每帧更新（消耗饱食度）
    // ========================================================================
    // @param delta_seconds 距离上次更新的时间（秒）
    // @note 仅在白天（6:00~22:00）消耗，每小时-1点
    void Tick(float delta_seconds);

    // ========================================================================
    // 【Consume】消耗饱食度
    // ========================================================================
    // @param amount 消耗的饱食度值（正值）
    void Consume(int amount);

    // ========================================================================
    // 【Restore】恢复饱食度
    // ========================================================================
    // @param amount 恢复的饱食度值
    void Restore(int amount);

    // ========================================================================
    // 【RestoreFromFood】从食材恢复饱食度（含品质加成）
    // ========================================================================
    // @param base_restore 食材基础饱食恢复值
    // @param quality_multiplier 品质倍率（Normal=1.0, Fine=1.3, Rare=1.8, Spirit=2.5, Holy=3.5）
    void RestoreFromFood(int base_restore, float quality_multiplier = 1.0f);

    // ========================================================================
    // 【RefillForNewDay】每天起床后重置饱食度
    // ========================================================================
    // @note 设为 80（基础饱足状态），吃早餐后恢复到 100
    void RefillForNewDay();

    // ========================================================================
    // 【HasEatenBreakfast】检查是否已吃过早餐
    // ========================================================================
    [[nodiscard]] bool HasEatenBreakfast() const { return has_eaten_breakfast_; }

    // ========================================================================
    // 【SetEatenBreakfast】标记早餐状态
    // ========================================================================
    void SetEatenBreakfast(bool value) { has_eaten_breakfast_ = value; }

    // ========================================================================
    // 【ResetDailyState】每天重置（睡觉时调用）
    // ========================================================================
    void ResetDailyState();

    // ========================================================================
    // 【状态查询】
    // ========================================================================
    [[nodiscard]] int Current() const noexcept { return current_; }
    [[nodiscard]] int Max() const noexcept { return max_; }
    [[nodiscard]] float Ratio() const noexcept {
        return max_ > 0 ? static_cast<float>(current_) / static_cast<float>(max_) : 0.0f;
    }
    [[nodiscard]] HungerState State() const noexcept;

    // ========================================================================
    // 【StaminaRecoveryMultiplier】体力恢复倍率
    // ========================================================================
    // @return 饱食状态对体力恢复速度的影响倍率
    [[nodiscard]] float StaminaRecoveryMultiplier() const noexcept;

    // ========================================================================
    // 【StaminaCostMultiplier】体力消耗倍率
    // ========================================================================
    // @return 饱食状态对体力消耗的影响倍率
    [[nodiscard]] float StaminaCostMultiplier() const noexcept;

    // ========================================================================
    // 【IsInDanger】是否处于危险饱食状态
    // ========================================================================
    [[nodiscard]] bool IsInDanger() const noexcept {
        return current_ < 20;
    }

    // ========================================================================
    // 【HungerHint】获取饱食度状态提示文本
    // ========================================================================
    [[nodiscard]] std::string HungerHint() const;

    // ========================================================================
    // 【存档接口】
    // ========================================================================
    [[nodiscard]] std::string SaveState() const;
    void LoadState(const std::string& state);

private:
    void ApplyClamp_();

    int current_ = 80;
    int max_ = 100;
    float accumulated_seconds_ = 0.0f;
    bool has_eaten_breakfast_ = false;
    const class GameClock* clock_ = nullptr;

    // 每小时消耗1点饱食度，仅白天生效
    static constexpr int kHungerPerHour = 1;
    // 每 60 秒检查一次饱食消耗
    static constexpr float kHungerTickInterval = 60.0f;
};

} // namespace CloudSeamanor::domain
