#pragma once

// ============================================================================
// 【Stamina】玩家体力能量系统
// ============================================================================
// 统一管理玩家的体力上限、当前值、消耗与恢复，支持新手保护期规则。
//
// 主要职责：
// - 管理体力上限和当前值
// - 处理体力消耗（移动、交互等）
// - 处理体力恢复（休息、灵茶、灵兽陪伴等）
// - 新手保护期消耗减半
//
// 与其他系统的关系：
// - 依赖：GameConfig（读取配置参数）
// - 被依赖：GameApp（主循环中更新体力消耗和恢复）
// - 被依赖：GameAppHud（UI显示体力条）
//
// 线程安全：无（游戏主线程单线程运行）
// ============================================================================

#include <string>

namespace CloudSeamanor::domain {

// ============================================================================
// 【StaminaSystem】体力能量管理类
// ============================================================================
// 管理玩家的体力能量，支持配置驱动的参数和新手保护期。
//
// 使用示例：
// @code
// StaminaSystem stamina;
// stamina.Initialize(config);  // 从配置初始化
// stamina.Consume(10.0f);     // 消耗体力
// stamina.Recover(5.0f);       // 恢复体力
// stamina.Update(delta_time);   // 每帧更新
// @endcode
class StaminaSystem {
public:
    // ========================================================================
    // 【Initialize】从配置初始化体力系统
    // ========================================================================
    // @param current 初始体力值
    // @param max_value 最大体力上限
    // @param protection_days 新手保护天数（此期间消耗减半）
    // @param protection_modifier 保护期消耗倍率
    void Initialize(
        float current,
        float max_value,
        int protection_days = 14,
        float protection_modifier = 0.7f
    );

    // ========================================================================
    // 【SetGameDay】设置当前游戏天数（用于新手保护判断）
    // ========================================================================
    // @param day 当前游戏天数（从1开始）
    void SetGameDay(int day);

    // ========================================================================
    // 【Consume】消耗体力
    // ========================================================================
    // @param amount 消耗的体力值（正值）
    // @note 新手保护期内实际消耗 = amount * protection_modifier_
    void Consume(float amount);

    // ========================================================================
    // 【ConsumeRaw】原始消耗（不受新手保护影响）
    // ========================================================================
    // 用于某些应该全额消耗的特殊行为（如强制结算扣体力）
    // @param amount 消耗的体力值
    void ConsumeRaw(float amount);

    // ========================================================================
    // 【Recover】恢复体力
    // ========================================================================
    // @param amount 恢复的体力值
    void Recover(float amount);

    // ========================================================================
    // 【RecoverPerSecond】按时间恢复体力
    // ========================================================================
    // @param amount_per_second 每秒恢复量
    // @param delta_time 距离上次恢复经过的时间（秒）
    void RecoverPerSecond(float amount_per_second, float delta_time);

    // ========================================================================
    // 【Refill】完全恢复体力到上限
    // ========================================================================
    // 用于睡觉到第二天或强制恢复场景
    void Refill();

    // ========================================================================
    // 【Update】每帧更新（恢复逻辑）
    // ========================================================================
    // @param delta_time 距离上次更新的时间（秒）
    // @param recover_per_second 每秒恢复量
    // @note 将根据游戏天数判断是否在保护期
    void Update(float delta_time, float recover_per_second);

    // ========================================================================
    // 【SetMax】设置体力上限
    // ========================================================================
    // @param max_value 新的体力上限（必须为正数）
    // @post 当前体力值不会超过新的上限
    void SetMax(float max_value);

    // ========================================================================
    // 【SetCurrent】设置当前体力
    // ========================================================================
    // @param current_value 新的体力值（将被限制在[0, max_]范围内）
    void SetCurrent(float current_value);

    // ========================================================================
    // 【IsInProtectionPeriod】是否处于新手保护期
    // ========================================================================
    // @return true 如果当前天数 <= 保护天数
    [[nodiscard]] bool IsInProtectionPeriod() const noexcept;

    // ========================================================================
    // 【CanAfford】是否能负担指定体力消耗
    // ========================================================================
    // @param amount 需要消耗的体力
    // @return true 如果当前体力 >= 消耗量
    [[nodiscard]] bool CanAfford(float amount) const noexcept;

    // ========================================================================
    // 【GetConsumptionModifier】获取当前消耗倍率
    // ========================================================================
    // @return 保护期内返回protection_modifier_，否则返回1.0f
    [[nodiscard]] float GetConsumptionModifier() const noexcept;

    // ========================================================================
    // 【状态查询接口】
    // ========================================================================
    [[nodiscard]] float Current() const noexcept { return current_; }
    [[nodiscard]] float Max() const noexcept { return max_; }
    [[nodiscard]] float Ratio() const noexcept {
        return max_ > 0.0f ? current_ / max_ : 0.0f;
    }
    [[nodiscard]] bool IsLow() const noexcept { return Ratio() < 0.2f; }
    [[nodiscard]] bool IsCritical() const noexcept { return Ratio() < 0.1f; }
    [[nodiscard]] bool IsFull() const noexcept { return current_ >= max_; }

private:
    // ========================================================================
    // 【ApplyClamp】将当前体力限制在合法范围内
    // ========================================================================
    void ApplyClamp_();

    // ========================================================================
    // 成员变量
    // ========================================================================
    float current_ = 120.0f;           // 当前体力值
    float max_ = 350.0f;               // 体力上限

    int game_day_ = 1;                 // 当前游戏天数
    int protection_days_ = 14;          // 新手保护天数
    float protection_modifier_ = 0.7f; // 保护期消耗倍率（消耗 * 0.7）
};

}  // namespace CloudSeamanor::domain
