#pragma once

// ============================================================================
// 【GameClock】游戏时间与日期管理系统
// ============================================================================
// 统一管理游戏的日期（天、季节、年份）和当日时间流逝。
//
// 主要职责：
// - 维护游戏日期（当前天数、季节、年份）
// - 管理当日时间流逝（分钟计时器）
// - 提供时间查询接口（小时、分钟、时段）
// - 支持存档/读档
//
// 与其他系统的关系：
// - 依赖：无（纯领域层）
// - 被依赖：GameApp（主循环计时）、CloudSystem（跨日刷新）、
//           GameAppHud（时间显示）、GameAppSave（存档/读档）
//
// 时间设计：
// - 1天 = 1440分钟（24小时 x 60分钟）
// - 游戏时间与实时时间通过 time_scale 关联
// - 默认每日时间段：晨(6-12)、昼(12-18)、昏(18-22)、夜(22-6)
// ============================================================================

#include <string>

namespace CloudSeamanor::domain {

// ============================================================================
// 【Season】游戏季节枚举
// ============================================================================
// 季节影响：作物生长速度、采集物类型、节日触发、NPC对话等。
//
// 时间表：
// - 春：第1天 ~ 第28天
// - 夏：第29天 ~ 第56天
// - 秋：第57天 ~ 第84天
// - 冬：第85天 ~ 第112天
// - 年循环：每112天为一年
enum class Season {
    Spring = 0,  // 春
    Summer = 1,   // 夏
    Autumn = 2,   // 秋
    Winter = 3,   // 冬
};

// ============================================================================
// 【DayPhase】一天内的时间段
// ============================================================================
// 用于天气表现、NPC对话、HUD显示等系统提供时段信息。
//
// 时间划分：
// - Morning（晨）：06:00 - 12:00，农活核心时段
// - Afternoon（昼）：12:00 - 18:00，经营与探索时段
// - Evening（昏）：18:00 - 22:00，社交与休闲时段
// - Night（夜）：22:00 - 06:00，收尾与休息时段
enum class DayPhase {
    Morning,    // 清晨
    Afternoon,  // 下午
    Evening,    // 傍晚
    Night,      // 夜晚
};

// ============================================================================
// 【GameClock】游戏时钟领域对象
// ============================================================================
// 管理游戏时间和日期，提供时间推进和查询接口。
//
// 设计决策：
// - 最小时间单位为分钟（float类型支持亚分钟精度）
// - 季节根据天数自动计算
// - 时间段根据小时自动判定
//
// 使用示例：
// @code
// GameClock clock;
// clock.Tick(delta_seconds);  // 推进时间
// clock.SleepToNextMorning();  // 睡到明天
// if (clock.CurrentPhase() == DayPhase::Evening) { ... }
// @endcode
class GameClock {
public:
    // ============================================================================
    // 【SeasonChangedEvent】季节变更事件
    // ============================================================================
    struct SeasonChangedEvent {
        bool changed = false;
        CloudSeamanor::domain::Season from = CloudSeamanor::domain::Season::Spring;
        CloudSeamanor::domain::Season to = CloudSeamanor::domain::Season::Spring;
    };

    // ========================================================================
    // 【常量定义】
    // ========================================================================
    static constexpr int kMinutesPerHour = 60;      // 每小时60分钟
    static constexpr int kHoursPerDay = 24;        // 每天24小时
    static constexpr int kMinutesPerDay = 1440;    // 每天1440分钟
    static constexpr int kDaysPerSeason = 28;     // 每季节28天
    static constexpr int kDaysPerYear = 112;      // 每年112天（4季节）

    // 时段边界（小时）
    static constexpr int kMorningStart = 6;       // 晨间开始
    static constexpr int kAfternoonStart = 12;     // 午后开始
    static constexpr int kEveningStart = 18;       // 傍晚开始
    static constexpr int kNightStart = 22;        // 夜间开始

    // ========================================================================
    // 【Initialize】初始化时钟
    // ========================================================================
    // @param day 初始天数（默认为1）
    // @param minutes 当日分钟数（默认为360，即06:00）
    void Initialize(int day = 1, float minutes = 360.0f);

    // ========================================================================
    // 【Tick】推进游戏时间
    // ========================================================================
    // @param delta_seconds 距离上次更新的秒数（已乘以time_scale）
    // @note 当分钟数超过1440时会自动进入下一天
    // @note 当天数超过112时会自动进入下一年
    void Tick(float delta_seconds);

    // ========================================================================
    // 【SleepToNextMorning】睡到第二天早晨
    // ========================================================================
    // @note 天数+1，时间重置为06:00（360分钟）
    // @note 用于玩家主动结束一天
    void SleepToNextMorning();

    // ========================================================================
    // 【AdvanceDay】推进到下一天（不重置时间）
    // ========================================================================
    // @note 用于强制跨日（如凌晨2点强制结算）
    void AdvanceDay();

    // ========================================================================
    // 【SetState】恢复时钟状态（用于读档）
    // ========================================================================
    // @param day 天数
    // @param minutes 当日分钟数
    void SetState(int day, float minutes);

    // ========================================================================
    // 【时间查询接口】
    // ========================================================================
    [[nodiscard]] int Day() const noexcept { return day_; }
    [[nodiscard]] float TimeOfDayMinutes() const noexcept { return minutes_in_day_; }

    // ========================================================================
    // 【Hour】获取当前小时（0-23）
    // ========================================================================
    [[nodiscard]] int Hour() const noexcept {
        return static_cast<int>(minutes_in_day_) / kMinutesPerHour;
    }

    // ========================================================================
    // 【Minute】获取当前分钟（0-59）
    // ========================================================================
    [[nodiscard]] int Minute() const noexcept {
        return static_cast<int>(minutes_in_day_) % kMinutesPerHour;
    }

    // ========================================================================
    // 【Season】获取当前季节
    // ========================================================================
    [[nodiscard]] Season Season() const noexcept;
    [[nodiscard]] DayPhase CurrentDayPhase() const noexcept;
    [[nodiscard]] int SeasonIndex() const noexcept;

    // ========================================================================
    // 【Year】获取当前年份（从1开始）
    // ========================================================================
    [[nodiscard]] int Year() const noexcept {
        return (day_ - 1) / kDaysPerYear + 1;
    }

    // ========================================================================
    // 【DayInSeason】当前是本季节第几天（1-28）
    // ========================================================================
    [[nodiscard]] int DayInSeason() const noexcept;

    // ========================================================================
    // 【DayInYear】当前是本年第几天（1-112）
    // ========================================================================
    [[nodiscard]] int DayInYear() const noexcept {
        return ((day_ - 1) % kDaysPerYear) + 1;
    }

    // ========================================================================
    // 【CurrentPhase】获取当前时段
    // ========================================================================
    [[nodiscard]] DayPhase CurrentPhase() const noexcept;

    // ========================================================================
    // 【TimeText】获取时间文本
    // ========================================================================
    // @return 格式如 "06:00"
    [[nodiscard]] std::string TimeText() const;

    // ========================================================================
    // 【DateText】获取日期文本
    // ========================================================================
    // @return 格式如 "春 第1天"
    [[nodiscard]] std::string DateText() const;

    // ========================================================================
    // 【FullDateText】获取完整日期文本
    // ========================================================================
    // @return 格式如 "第1年第28天 春"
    [[nodiscard]] std::string FullDateText() const;

    // ========================================================================
    // 【PhaseText】获取时段文本
    // ========================================================================
    [[nodiscard]] std::string PhaseText() const;

    // ========================================================================
    // 【IsNightTime】是否在夜间
    // ========================================================================
    [[nodiscard]] bool IsNightTime() const noexcept {
        return CurrentPhase() == DayPhase::Night;
    }

    // ========================================================================
    // 【IsRestTime】是否在休息时段（22:00后）
    // ========================================================================
    [[nodiscard]] bool IsRestTime() const noexcept {
        return Hour() >= kNightStart || Hour() < kMorningStart;
    }

    // ========================================================================
    // 【MinutesUntilPhase】距离指定时段的分钟数
    // ========================================================================
    // @param phase 目标时段
    // @return 到达该时段还需要多少分钟（负数表示已过）
    [[nodiscard]] float MinutesUntilPhase(DayPhase phase) const noexcept;

    // ========================================================================
    // 【SeasonName】获取季节名称
    // ========================================================================
    [[nodiscard]] static std::string SeasonName(CloudSeamanor::domain::Season s);

    // ========================================================================
    // 【PhaseName】获取时段名称
    // ========================================================================
    [[nodiscard]] static std::string PhaseName(DayPhase phase);

    // ============================================================================
    // 【ConsumeSeasonChangedEvent】消费季节变更事件
    // ============================================================================
    // 在跨季时返回 changed=true；调用后事件会被重置。
    [[nodiscard]] SeasonChangedEvent ConsumeSeasonChangedEvent() noexcept;

private:
    // ========================================================================
    // 【AdvanceYear】推进年份（内部）
    // ========================================================================
    void AdvanceYear_();
    void UpdateSeasonChangedEvent_() noexcept;

    // ========================================================================
    // 成员变量
    // ========================================================================
    int day_ = 1;                   // 当前天数（从1开始）
    float minutes_in_day_ = 360.0f; // 当日分钟数（06:00开始）
    CloudSeamanor::domain::Season cached_season_ = CloudSeamanor::domain::Season::Spring;
    SeasonChangedEvent pending_season_changed_event_{};
};

}  // namespace CloudSeamanor::domain
